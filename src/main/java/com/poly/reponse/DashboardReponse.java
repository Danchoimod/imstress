package com.poly.reponse;

public class DashboardReponse {


    private String label;
    private Long value;

    public void setLabel(String label) {
        this.label = label;
    }
    public void setValue(Long value) {
        this.value = value;
    }
    public String getLabel() {
        return label;
    }

    public Long getValue() {
        return value;
    }
}